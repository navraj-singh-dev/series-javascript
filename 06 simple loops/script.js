// 1. for loop (use when you know the number of iterations)
for (let i = 1; i <= 10; i++) {
  console.log(i);
}

// 2. while loop (use when you know a condition, but not number of iterations)
let i = 1;
while (i <= 5) {
  console.log(i);
  i++;
}

// 3. do while loop
let j = 10
do {
    console.log(j)
    j++
} while (j <= 5)
